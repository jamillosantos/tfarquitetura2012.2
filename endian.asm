
.include "macros.asm"

.globl endian_read_b
.globl endian_read_h
.globl endian_read_w

.data

.text

################################################################################
# Read a half byte
################################################################################
endian_read_b:
	lb $v0, 0($a0)
	jr $ra	

################################################################################
# Read a half word
################################################################################
endian_read_h:
	lb $v0, 0($a0)
	sll $v0, $v0, 8
	lb $t0, 1($a0)
	or $v0, $v0, $t0

	jr $ra

################################################################################
# Read a word
################################################################################
endian_read_w:
	lb $v0, 0($a0)
	sll $v0, $v0, 8
	lb $t0, 1($a0)
	and $t0, $t0, 0xff
	or $v0, $v0, $t0
	sll $v0, $v0, 8
	lb $t0, 2($a0)
	and $t0, $t0, 0xff
	or $v0, $v0, $t0
	sll $v0, $v0, 8
	lb $t0, 3($a0)
	and $t0, $t0, 0xff
	or $v0, $v0, $t0

	jr $ra

################################################################################
# Read signed word
# ----------------
# Por enquanto nenhuma diferen√ßa com a leitura de uma word
################################################################################
endian_read_sw:
	lb $v0, 0($a0)
	sll $v0, $v0, 8
	lb $t0, 1($a0)
	and $t0, $t0, 0xff
	or $v0, $v0, $t0
	sll $v0, $v0, 8
	lb $t0, 2($a0)
	and $t0, $t0, 0xff
	or $v0, $v0, $t0
	sll $v0, $v0, 8
	lb $t0, 3($a0)
	and $t0, $t0, 0xff
	or $v0, $v0, $t0

	jr $ra
